void SHA1_Init(SHA1_CTX* context)
{
#if SHA1_USE_ORIGINAL_IMPLEMENTATION
	_SHA1_Init(context);
#elif SHA1_USE_ALTERNATIVE_IMPLEMENTATION
	SHA1Reset(context);
#endif
}

void SHA1_Update(SHA1_CTX *context, unsigned char *data, unsigned int len)
{
#if SHA1_USE_ORIGINAL_IMPLEMENTATION
	_SHA1_Update(context, (sha1_byte *)data, len);
#elif SHA1_USE_ALTERNATIVE_IMPLEMENTATION
	SHA1Input(context, (const uint8_t *) data, (unsigned) len);
#endif
}

void SHA1_Final(unsigned char digest[SHA1_DIGEST_LENGTH], SHA1_CTX *context)
{
#if SHA1_USE_ORIGINAL_IMPLEMENTATION
	_SHA1_Final((sha1_byte *)digest, context);
#elif SHA1_USE_ALTERNATIVE_IMPLEMENTATION
	SHA1Result(context, digest);
#endif
}
